def make_river(start, end, heightmap):
    hm = heightmap
    all_nodes = heightmap
    return get_path(all_nodes, start, end, heightmap)


def get_cost(origin, next):
    h1 = heightmap[origin[0]][origin[1]]
    h2 = heightmap[next[0]][next[1]]
    return abs(h1 - h2)


def test_pypaths():
    finder = astar.pathfinder(cost=get_cost)
    p = finder(
        (0, 9), (9, 0)
    )
    print(p)
    finder = astar.pathfinder()
    p = finder(
        (0, 9), (9, 0)
    )
    print(p)


